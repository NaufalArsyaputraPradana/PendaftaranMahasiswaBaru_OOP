    public void getData() {
        int baris = tblMhs.getSelectedRow();
        txtNama.setText(String.valueOf(tblMhs.getValueAt(baris, 1)));
        txtNisn.setText(String.valueOf(tblMhs.getValueAt(baris, 2)));
        txtProdi.setText(String.valueOf(tblMhs.getValueAt(baris, 3)));
        txtAsal.setText(String.valueOf(tblMhs.getValueAt(baris, 4)));
        
    }

    public void insert(String nama, int nisn, String minat, String asal) {
        try {
            Class.forName(JDBC_DRIVER);

            conn = DriverManager.getConnection(DB_URL, USER, PASS);

            stmt = conn.createStatement();

            String sql = "INSERT INTO mhs (nama, nisn, prodi, asal) VALUES (?,?,?,?)";

            PreparedStatement pms = conn.prepareStatement(sql);
            pms.setString(1, nama);
            pms.setInt(2, nisn);
            pms.setString(3, minat);
            pms.setString(4, asal);


            pms.execute();

            stmt.close();
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void showtable() {
        try {
            Class.forName(JDBC_DRIVER);
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("Nama");
            model.addColumn("NISN");
            model.addColumn("Prodi Minat");
            model.addColumn("Asal Sekolah");

            stmt = conn.createStatement();
            String sql = "SELECT * FROM mhs";
            int i = 1;
            rs = stmt.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{
                    i,
                    rs.getString("nama"),
                    rs.getInt("nisn"),
                    rs.getString("prodi"),
                    rs.getString("asal"),

                });
                i++;
            }
            rs.close();
            conn.close();
            stmt.close();

            tblMhs.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

