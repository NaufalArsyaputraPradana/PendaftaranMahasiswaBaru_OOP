    public void update(String nama, int nisn, String minat, String asal) {
        try {
            Class.forName(JDBC_DRIVER);

            conn = DriverManager.getConnection(DB_URL, USER, PASS);

            stmt = conn.createStatement();

            String sql = "UPDATE mhs SET nama=?, nisn=?, prodi=?, asal=? WHERE id_mhs='" + nama + "'";

            PreparedStatement pms = conn.prepareStatement(sql);
            pms.setString(1, nama);
            pms.setInt(2, nisn);
            pms.setString(3, minat);
            pms.setString(4, asal);


            pms.execute();

            stmt.close();
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

